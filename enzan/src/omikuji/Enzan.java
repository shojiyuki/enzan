package omikuji;

public class Enzan {
	public static int add(int a, int b) {
		return a + b;
		
	}
	
	public static int sub(int a, int b) {
		return a - b;
		
	}
	
	public static int multiply(int a, int b) {
		return a * b;
		
	}
	
	public static int devide(int a, int b) {
		return a / b;
		
	}
	
	public static int mod(int a, int b) {
		return a % b;
		
	}
}
